#!/usr/bin/env python3
# -*- coding: utf-8 -*-
from __future__ import annotations

import hashlib
import json
from pathlib import Path
from typing import Any

from data_modules.story_contracts import StoryContractPaths, write_json
from data_modules.story_repair.outline_catalog import OutlineChapterRecord, load_outline_catalog
from data_modules.story_repair.provenance_auditor import audit_project


PROJECTION_TARGETS = ["state", "index", "summary", "memory"]


def build_rebuild_plan(
    project_root: Path,
    *,
    start: int,
    end: int,
    dry_run: bool = True,
    report_file: str | Path | None = None,
) -> dict[str, Any]:
    project_root = Path(project_root)
    audit = audit_project(project_root, start=start, end=end)
    chapters = list(range(start, end + 1))
    plan = {
        "mode": "dry_run" if dry_run else "apply",
        "chapters_to_rebuild_contracts": chapters,
        "chapters_to_rebuild_commits": chapters,
        "stale_files_to_archive": audit["stale_files"],
        "projection_targets": PROJECTION_TARGETS,
        "requires_human_review": True,
        "audit_summary": {
            "repair_required": [row["chapter"] for row in audit["chapters"] if row["repair_required"]],
            "numbering_anomalies": audit["numbering_anomalies"],
        },
    }
    if report_file:
        write_rebuild_report(project_root, plan, report_file)
    return plan


def write_rebuild_report(project_root: Path, plan: dict[str, Any], report_file: str | Path) -> Path:
    target = Path(report_file)
    if not target.is_absolute():
        target = Path(project_root) / target
    target.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "# Story Repair Dry Run",
        "",
        f"- Mode: {plan['mode']}",
        f"- Rebuild contracts: {plan['chapters_to_rebuild_contracts']}",
        f"- Rebuild commits: {plan['chapters_to_rebuild_commits']}",
        f"- Projection targets: {', '.join(plan['projection_targets'])}",
        f"- Requires human review: {plan['requires_human_review']}",
        "",
        "Backfilled commits generated by repair mode are historical reconstruction only; they do not prove the original write context.",
        "Review MASTER_SETTING before apply because prior chapter refreshes may have polluted book-level settings.",
        "",
        "```json",
        json.dumps(plan, ensure_ascii=False, indent=2),
        "```",
    ]
    target.write_text("\n".join(lines) + "\n", encoding="utf-8")
    return target


def _read_json(path: Path, default: Any) -> Any:
    if not path.is_file():
        return default
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except json.JSONDecodeError:
        return default


def _sha256_file(path: Path) -> str:
    return hashlib.sha256(path.read_bytes()).hexdigest() if path.is_file() else ""


def _catalog_by_chapter(project_root: Path) -> dict[int, OutlineChapterRecord]:
    return {record.global_chapter: record for record in load_outline_catalog(project_root)}


def _ledger_by_chapter(ledger: dict[str, Any]) -> dict[int, dict[str, Any]]:
    rows = ledger.get("chapters") if isinstance(ledger.get("chapters"), list) else []
    return {int(row["chapter"]): row for row in rows if isinstance(row, dict) and row.get("chapter")}


def _body_text(project_root: Path, row: dict[str, Any]) -> str:
    source = str(row.get("source_body") or "").strip()
    path = project_root / source if source else Path()
    return path.read_text(encoding="utf-8") if path.is_file() else ""


def _chapter_contract(chapter: int, record: OutlineChapterRecord, row: dict[str, Any]) -> dict[str, Any]:
    return {
        "meta": {
            "schema_version": "story-system/v1",
            "contract_type": "CHAPTER_BRIEF",
            "chapter": chapter,
            "volume": record.volume,
            "chapter_in_volume": record.chapter_in_volume,
            "repair_generated": True,
        },
        "override_allowed": {"chapter_focus": record.goal or row.get("title", "")},
        "chapter_directive": {
            "goal": record.goal or row.get("title", ""),
            "time_anchor": record.time_anchor or row.get("time_anchor", ""),
            "chapter_span": record.chapter_span,
            "hook": record.hook,
            "chapter_end_open_question": record.end_open_question,
            "key_entities": list(record.key_entities),
            "source": "outline_catalog",
        },
        "dynamic_context": [],
        "source_trace": [
            {
                "source_file": record.source_file,
                "outline_hash": record.outline_hash,
                "repair_role": "editorial_canon_outline",
            }
        ],
        "provenance": {
            "outline_hash": record.outline_hash,
            "source_body": row.get("source_body", ""),
            "commit_mode": "repair_backfill",
        },
    }


def _review_contract(chapter: int, record: OutlineChapterRecord) -> dict[str, Any]:
    checks = [record.goal] if record.goal else []
    if record.hook:
        checks.append(record.hook)
    return {
        "meta": {
            "schema_version": "story-system/v1",
            "contract_type": "REVIEW_CONTRACT",
            "chapter": chapter,
            "repair_generated": True,
        },
        "must_check": checks,
        "blocking_rules": [],
        "genre_specific_risks": [],
        "anti_patterns": [],
        "system_constraints": [],
        "review_thresholds": {"blocking_count": 0, "missed_nodes": 0},
        "overrides": {"locked": {}, "append_only": {}, "override_allowed": {}},
        "provenance": {"outline_hash": record.outline_hash, "commit_mode": "repair_backfill"},
    }


def _volume_contract(volume: int) -> dict[str, Any]:
    return {
        "meta": {
            "schema_version": "story-system/v1",
            "contract_type": "VOLUME_BRIEF",
            "volume": volume,
            "repair_generated": True,
        },
        "volume_goal": {"summary": f"第{volume}卷修复后主链合同"},
        "selected_tropes": [],
        "selected_pacing": {},
        "selected_scenes": [],
        "anti_patterns": [],
        "system_constraints": [],
        "overrides": {"locked": {}, "append_only": {}, "override_allowed": {}},
    }


def _commit_payload(project_root: Path, chapter: int, record: OutlineChapterRecord, row: dict[str, Any]) -> dict[str, Any]:
    source_body = str(row.get("source_body") or "")
    body_path = project_root / source_body if source_body else Path()
    body_hash = _sha256_file(body_path)
    summary = str(row.get("summary") or row.get("exit_state") or "").strip()
    return {
        "meta": {"schema_version": "story-system/v1", "chapter": chapter, "status": "accepted"},
        "contract_refs": {
            "master": "MASTER_SETTING.json",
            "volume": f"volume_{record.volume:03d}.json",
            "chapter": f"chapter_{chapter:03d}.json",
            "review": f"chapter_{chapter:03d}.review.json",
        },
        "provenance": {
            "write_fact_role": "chapter_commit",
            "commit_mode": "repair_backfill",
            "body_sha256": body_hash,
            "outline_sha256": record.outline_hash,
            "contract_sha256": "",
            "legacy_state_role": "projection_only",
        },
        "outline_snapshot": {
            "planned_nodes": [record.goal] if record.goal else [],
            "covered_nodes": [record.goal] if record.goal else [],
            "missed_nodes": [],
            "extra_nodes": [],
        },
        "review_result": {"blocking_count": 0},
        "fulfillment_result": {
            "planned_nodes": [record.goal] if record.goal else [],
            "covered_nodes": [record.goal] if record.goal else [],
            "missed_nodes": [],
            "extra_nodes": [],
        },
        "disambiguation_result": {"pending": []},
        "accepted_events": list(row.get("must_keep_events") or []),
        "state_deltas": [],
        "entity_deltas": [],
        "entities_appeared": [{"canonical_name": name} for name in row.get("characters") or []],
        "scenes": [],
        "chapter_meta": {
            "title": row.get("title") or record.title,
            "time_anchor": row.get("time_anchor") or record.time_anchor,
            "location": row.get("location", ""),
        },
        "dominant_strand": "",
        "summary_text": summary,
        "projection_status": {name: "done" for name in ["state", "index", "summary", "memory", "vector"]},
    }


def _ensure_master(paths: StoryContractPaths) -> None:
    if paths.master_json.is_file():
        return
    write_json(
        paths.master_json,
        {
            "meta": {"schema_version": "story-system/v1", "contract_type": "MASTER_SETTING", "repair_generated": True},
            "route": {"primary_genre": "repaired"},
            "master_constraints": {},
            "base_context": [],
            "source_trace": [],
            "override_policy": {},
        },
    )


def _update_state(project_root: Path, end: int, catalog: dict[int, OutlineChapterRecord]) -> None:
    state_path = project_root / ".webnovel" / "state.json"
    state = _read_json(state_path, {})
    if not isinstance(state, dict):
        state = {}
    progress = state.setdefault("progress", {})
    progress["current_chapter"] = end
    progress["volumes_planned"] = _volume_ranges(catalog)
    chapter_status = progress.setdefault("chapter_status", {})
    for chapter in range(1, end + 1):
        chapter_status[str(chapter)] = "chapter_committed"
    protagonist_state = state.setdefault("protagonist_state", {})
    location = protagonist_state.setdefault("location", {})
    location["last_chapter"] = end
    state_path.parent.mkdir(parents=True, exist_ok=True)
    state_path.write_text(json.dumps(state, ensure_ascii=False, indent=2), encoding="utf-8")


def _volume_ranges(catalog: dict[int, OutlineChapterRecord]) -> list[dict[str, Any]]:
    max_by_volume: dict[int, int] = {}
    for record in catalog.values():
        max_by_volume[record.volume] = max(max_by_volume.get(record.volume, 0), record.chapter_in_volume)
    return [
        {"volume": volume, "chapters_range": f"1-{count}", "updated_by": "story_repair"}
        for volume, count in sorted(max_by_volume.items())
    ]


def _write_summaries(project_root: Path, chapter: int, row: dict[str, Any], body_text: str) -> None:
    summary_dir = project_root / ".webnovel" / "summaries"
    summary_dir.mkdir(parents=True, exist_ok=True)
    summary = str(row.get("summary") or row.get("exit_state") or "").strip()
    if not summary:
        summary = body_text[:500].strip()
    (summary_dir / f"ch{chapter:04d}.md").write_text(
        f"# 第{chapter}章 修复摘要\n\n## 剧情摘要\n{summary}\n",
        encoding="utf-8",
    )


def apply_rebuild_from_ledger(
    project_root: Path,
    *,
    start: int,
    end: int,
    ledger_path: str | Path,
) -> dict[str, Any]:
    project_root = Path(project_root)
    ledger_file = Path(ledger_path)
    if not ledger_file.is_absolute():
        ledger_file = project_root / ledger_file
    ledger = _read_json(ledger_file, {})
    if ledger.get("ledger_status") != "accepted_for_repair":
        raise ValueError("ledger_status must be accepted_for_repair before apply")

    paths = StoryContractPaths.from_project_root(project_root)
    for directory in (paths.chapters_dir, paths.reviews_dir, paths.volumes_dir, paths.commits_dir):
        directory.mkdir(parents=True, exist_ok=True)
    paths.root.mkdir(parents=True, exist_ok=True)
    _ensure_master(paths)
    if not paths.anti_patterns_json.is_file():
        write_json(paths.anti_patterns_json, [])

    catalog = _catalog_by_chapter(project_root)
    ledger_rows = _ledger_by_chapter(ledger)
    applied: list[int] = []
    for chapter in range(start, end + 1):
        record = catalog.get(chapter)
        row = ledger_rows.get(chapter)
        if record is None or row is None:
            raise ValueError(f"ledger missing chapter {chapter}")
        write_json(paths.volume_json(record.volume), _volume_contract(record.volume))
        write_json(paths.chapter_json(chapter), _chapter_contract(chapter, record, row))
        write_json(paths.review_json(chapter), _review_contract(chapter, record))
        body = _body_text(project_root, row)
        _write_summaries(project_root, chapter, row, body)
        write_json(paths.commit_json(chapter), _commit_payload(project_root, chapter, record, row))
        applied.append(chapter)

    next_record = catalog.get(end + 1)
    if next_record is not None:
        next_row = {
            "chapter": end + 1,
            "title": next_record.title,
            "time_anchor": next_record.time_anchor,
            "source_body": "",
        }
        write_json(paths.volume_json(next_record.volume), _volume_contract(next_record.volume))
        write_json(paths.chapter_json(end + 1), _chapter_contract(end + 1, next_record, next_row))
        write_json(paths.review_json(end + 1), _review_contract(end + 1, next_record))

    _update_state(project_root, end, catalog)
    return {"applied": True, "chapters": applied, "next_chapter_prepared": bool(next_record)}
